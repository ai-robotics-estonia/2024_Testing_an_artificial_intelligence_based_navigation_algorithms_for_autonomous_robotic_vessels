def get_obstacle_locations_and_types(occupancy_map, type_dict: dict):
    locations = []
    types = []
    x_dim = occupancy_map.shape[1]  # ?
    y_dim = occupancy_map.shape[0]
    for j in range(y_dim):
        for i in range(x_dim):
            type_id = occupancy_map[j][i]  # ?
            if type_id != 0:
                type = type_dict[type_id]
                locations.append([j, i])
                types.append(type)
    return locations, types, len(locations)


